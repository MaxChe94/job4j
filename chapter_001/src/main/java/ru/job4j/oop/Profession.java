package ru.job4j.oop;

public class Profession {
    String name;
    String surname;
    String education;
    String birthday;

    public String getName () {
        return this.name;
    }

    public String getSurame () {
        return this.surname;
    }

    public String getEducation () {
        return this.education;
    }

    public String getBirthday () {
        return this.birthday;
    }


}
